    def din2dt(self,din,end_dt=None):
        """
        Converts input dates to datetime objects.
        Inputs can be strings in YYYY-mm-dd format, string "TODAY",
        or integer implying days in the past from today or end_dt if given
        """
        today = datetime.date.today()
        today_dt = datetime.datetime(year=today.year,month=today.month,day=today.day)
        if din == "TODAY":
            return today_dt
        elif type(din) == type(0):
            if type(end_dt) == type(today_dt):
                return end_dt - datetime.timedelta(days=din)
            else:
                return today_dt - datetime.timedelta(days=din)
        else:
            return datetime.datetime.strptime(din,"%Y-%m-%d")


    def link2slug(self,link):
        """
        Takes a url like "vinepair.com/articles/best_wine?urlterms" and returns
        the "slug" that represents the unique content title "best_wine"
        """
        if type(link) == type([]): #some text comes in a list
            link = link[0].strip()
        if len(link) > 0: #if nonzero string
            if link[-1] == "/": #remove trailing slashes
                link = link[0:-1]
            slug = link.split("/")[-1].strip()
        else:
            slug = "null"
        if len(slug) == 0:
            slug = "null"
        slug = slug.split("?")[0].strip()
        if len(slug) == 0:
            slug = "null"
        return slug.encode('utf-8','ignore')

    def response2df(self,response):
        """
        Originally converted query responses to Pandas DF by creating an intermediate
        list of dictionaries, now returns that list directly to eliminate use of Pandas
        each list element is a dict of query data for a single page on a single date
        each dict contains keys for fields (e.g. pageviews), values are counts
        """
        dlist = []
        # get report data
        for report in response.get('reports', []):
            # set column headers
            columnHeader = report.get('columnHeader', {})
            dimensionHeaders = columnHeader.get('dimensions', [])
            metricHeaders = columnHeader.get('metricHeader', {}).get('metricHeaderEntries', [])
            rows = report.get('data', {}).get('rows', [])
    
            for row in rows:
                # create dict for each row
                ddict = {}
                dimensions = row.get('dimensions', [])
                dateRangeValues = row.get('metrics', [])

                # fill dict with dimension header (key) and dimension value (value)
                for header, dimension in zip(dimensionHeaders, dimensions):
                    ddict[header] = dimension

                # fill dict with metric header (key) and metric value (value)
                for i, values in enumerate(dateRangeValues):
                    for metric, value in zip(metricHeaders, values.get('values')):
                        #set int as int, float a float
                        if ',' in value or '.' in value:
                            ddict[metric.get('name')] = float(value)
                        else:
                            ddict[metric.get('name')] = int(value)

                dlist.append(ddict)
        #here, eliminate use of Pandas
        #df = pd.DataFrame(dlist)
        return dlist
    
    def ts2json(self,tsa,source_array,to_output,output_file="test.json",start=None,end=None,all_caps=None,frozen=None):
        """
        Takes a monthly TsArray object, source_array and generates custom JSON 
        format for VP fronend (amCharts v4)
        If frozen== score or subscore, frozen data (below) are used in-place of passed data
        """
        data = tsa.arrays[source_array].copy()
        if frozen == "score":
            fz_data = self.frozen_data("score")
        elif frozen == "subscore":
            fz_data = self.frozen_data("subscore")
        else:
            fz_data = None
        all_month_dt = tsa.dt_list
        if start is not None and end is not None:
            all_dt = all_month_dt
            start_dt = self.din2dt(start)
            end_dt = self.din2dt(end)
            valid_index = list(i for i in range(len(all_month_dt)) if all_dt[i] >= start_dt and all_dt[i] <= end_dt)
            data = data[:,valid_index]
            all_month_dt = list(all_month_dt[i] for i in valid_index)
        all_mo_dstr = list(dt.strftime("%b-%Y") for dt in all_month_dt)
        if all_caps is not None:
            to_capitalize = list(word.lower() for word in all_caps)
        else:
            to_capitalize = []
        #accumulate a list of dictionaries, each dictionary is one month of data
        mo_dlist = []        
        for mi,month in enumerate(all_mo_dstr):
            mdict = {}
            mdict["MONTH"] = month
            for ri,row in enumerate(data):
                gname = tsa.r2p[ri]
                if gname not in to_output:
                    continue
                output = []
                for word in gname.split():
                    lword = word.strip().lower()
                    if lword in to_capitalize:
                        cword = lword.upper()
                    else:
                        cword = lword.capitalize()
                    output.append(cword)
                gname_out = " ".join(output)
                if fz_data is not None and month in fz_data:
                    val = fz_data[month][gname_out]
                else:
                    val = row[mi]
                if val != val:
                    val = 0.0 #replaces NaN with 0
                mdict[gname_out] = float(val)
            mo_dlist.append(mdict)
        f = open(output_file,'w')
        print >> f,"[", #prepend bracket
        for di,d in enumerate(mo_dlist):
            json_out=json.dumps(d,indent=5)
            print >> f,json_out,#actual json
            if di < len(mo_dlist)-1:
                print >> f,",", #add comma if not last element

        print >> f,"]\n" #postpend bracket and newline
        f.close()

